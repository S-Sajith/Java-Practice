package O25.Serialization;

import java.io.Serializable;

public class Person implements Serializable { //implement this to make a class serializable
    private int id;
    private String name;

    public Person(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
